package project.fifteen.BettingWindow;

public class Result {
	private int home, visitor;
	
	public Result() {
		this.home = 0;
		this.visitor = 0;
	}

	public int getHome() {
		return home;
	}

	public void setHome(int home) {
		this.home = home;
	}

	public int getVisitor() {
		return visitor;
	}

	public void setVisitor(int visitor) {
		this.visitor = visitor;
	}
	
}